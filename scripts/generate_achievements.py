def strip_trailing_numbers(text: str) -> str:
    """Remove trailing numbers like .21, .23, .13 from description."""
    return re.sub(r'\s*\d+\s*$', '', text).strip()


def parse_achievements() -> list[dict]:
    """Parse raw text into structured achievements."""
    achievements = []
    current_section = 0
    sort_order = 0

    for line in RAW_ACHIEVEMENTS.strip().split('\n'):
        line = line.strip()
        if not line:
            continue
        if line.startswith('Раздел '):
            current_section = int(line[7])  # "Раздел 1:" -> 1
            continue
        if not line.startswith('Знак '):
            continue

        # Split on first ": " to get title and description
        if ': ' not in line:
            continue
        idx = line.index(': ')
        title_ru = line[:idx].strip()
        desc_ru = strip_trailing_numbers(line[idx + 2:].strip())

        symbols = SYMBOLS_BY_SECTION.get(current_section, ['star.fill'])
        section_count = len([a for a in achievements if a.get('section') == current_section])
        symbol = symbols[section_count % len(symbols)]

        # Placeholder EN/UK: use RU for now (to be refined)
        title_en = title_ru  # placeholder
        title_uk = title_ru  # placeholder
        desc_en = desc_ru   # placeholder
        desc_uk = desc_ru   # placeholder

        achievements.append({
            'id': str(uuid.uuid4()),
            'section': current_section,
            'sort_order': sort_order,
            'symbol': symbol,
            'title_ru': title_ru,
            'title_en': title_en,
            'title_uk': title_uk,
            'description_ru': desc_ru,
            'description_en': desc_en,
            'description_uk': desc_uk,
        })
        sort_order += 1

    return achievements


